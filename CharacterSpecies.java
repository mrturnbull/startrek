public class CharacterSpecies {

  private String uid;
  private String name;
  private int numerator;
  private int denominator;

  public CharacterSpecies(){}

	/**
	* Returns value of uid
	* @return
	*/
	public String getUid() {
		return uid;
	}

	/**
	* Sets new value of uid
	* @param
	*/
	public void setUid(String uid) {
		this.uid = uid;
	}

	/**
	* Returns value of name
	* @return
	*/
	public String getName() {
		return name;
	}

	/**
	* Sets new value of name
	* @param
	*/
	public void setName(String name) {
		this.name = name;
	}

	/**
	* Returns value of numerator
	* @return
	*/
	public int getNumerator() {
		return numerator;
	}

	/**
	* Sets new value of numerator
	* @param
	*/
	public void setNumerator(int numerator) {
		this.numerator = numerator;
	}

	/**
	* Returns value of denominator
	* @return
	*/
	public int getDenominator() {
		return denominator;
	}

	/**
	* Sets new value of denominator
	* @param
	*/
	public void setDenominator(int denominator) {
		this.denominator = denominator;
	}
}
